package bigPackage.Interfaces;

/**
 * The interface Turbo.
 */
public interface ITurbo {
    /**
     * Is turbo on boolean
     *
     * @return the boolean
     */
    boolean isTurboOn();

    /**
     * Turns on turbo
     */
    void setTurboOn();

    /**
     * Turns off turbo
     */
    void setTurboOff();
}
